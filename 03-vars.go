package main

import "fmt"

func main() {
	var a string = "Go Lang"
	var b int = 10
	var c float32 = 3.14
	var d bool = false 
        
	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
}


/*

	Abra um terminal ou o shell e digite o seguinte comando:
	
		go run 03-vars.go
	
	
	Você terá como resposta:
	
		Go Lang
		10
		3.14
		false

*/
